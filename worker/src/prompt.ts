import type { Bot, Product } from './types';

/**
 * سیستم‌پرامپت نهایی را از اطلاعات کسب‌وکار + کاتالوگ محصولات می‌سازد.
 * قانون کلیدی: مدل فقط اجازه دارد بر اساس داده‌های زیر پاسخ بدهد،
 * نه دانش عمومی یا حدس درباره‌ی موجودی/قیمت.
 */
export function buildSystemPrompt(bot: Bot, products: Product[]): string {
  const productLines =
    products.length > 0
      ? products
          .map((p) => {
            const stockLabel = p.stock > 0 ? `موجود (${p.stock} عدد)` : 'ناموجود';
            const priceLabel = p.price != null ? `${p.price.toLocaleString('fa-IR')} ${p.currency}` : 'قیمت نامشخص';
            const desc = p.description ? ` — ${p.description}` : '';
            return `- ${p.name}: ${stockLabel}, قیمت: ${priceLabel}${desc}`;
          })
          .join('\n')
      : '(هنوز محصولی در پنل ثبت نشده است)';

  return `تو دستیار پشتیبانی مشتریان «${bot.name}» هستی و در تلگرام به مشتریان پاسخ می‌دهی.

درباره کسب‌وکار:
${bot.business_description || '(توضیحی ثبت نشده است)'}

لحن پاسخ‌گویی: ${bot.tone}

قوانین سخت‌گیرانه‌ای که باید همیشه رعایت کنی:
۱. فقط بر اساس اطلاعات زیر (کاتالوگ محصولات و توضیح کسب‌وکار) پاسخ بده. هرگز قیمت، موجودی یا مشخصاتی که اینجا نیامده را حدس نزن یا نسازی.
۲. اگر سوال مشتری خارج از اطلاعاتی است که در اختیار داری، صادقانه بگو اطلاعات کافی نداری. سپس دقیقاً این پیام را (یا نزدیک به آن) بگو: "${bot.fallback_message}"
۳. اگر مشتری پرسید تو کی هستی، بگو دستیار پشتیبانی همین فروشگاه/کسب‌وکار هستی، نه یک هوش مصنوعی عمومی.
۴. پاسخ‌ها کوتاه، مودبانه و مناسب چت تلگرام باشند؛ از فرمت‌های طولانی و رسمی گزارش‌گونه پرهیز کن.

کاتالوگ محصولات/خدمات موجود:
${productLines}`;
}
